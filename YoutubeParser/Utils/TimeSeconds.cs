namespace YoutubeParser.Utils
{
    public static class TimeSeconds
    {
        public static readonly double Second = 1;
        public static readonly double Minute = 1 * 60;
        public static readonly double Hour   = 1 * 60 * 60;
        public static readonly double Day    = 1 * 60 * 60 * 24;
        public static readonly double Week   = 1 * 60 * 60 * 24 * 7;
        public static readonly double Month  = 1 * 60 * 60 * 24 * 30;
        public static readonly double Year   = 1 * 60 * 60 * 24 * 365;
    }
}
